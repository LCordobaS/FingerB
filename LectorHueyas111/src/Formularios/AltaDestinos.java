    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Connection con= null;
        PreparedStatement stmt = null;
        ResultSet rs = null;
        EmpleadoBean empleado= null;
        try{
            con= Conexion.getConexion();
            stmt = con.prepareStatement("SELECT * FROM empleado WHERE clave = ?");
            stmt.setInt(1, Integer.parseInt (txtClave.getText()));
            
            rs = stmt.executeQuery();
            
            if(rs.next()){
               txtId.setText(rs.getString("clave"));
               txtAPaterno.setText(rs.getString("apPaterno"));
               txtAMaterno.setText(rs.getString("apMaterno"));
                txtNombre.setText(rs.getString("nombre"));
               cbCarrera.setSelectedItem(rs.getString("NombreCarrera"));
               cbCuatri.setSelectedItem(rs.getString("NombreCuatrimestre"));
               cbGrupo.setSelectedItem(rs.getString("NombreGrupo"));
            } else{
                JOptionPane.showMessageDialog(null,"No existe ninguna clave vuelva a intentarlo");
                
            }
            
            con.close();
        } catch(Exception e){
            
            System.err.println(e);
            
        }

       
    
    }//GEN-LAST:event_btnBuscarActionPerformed

